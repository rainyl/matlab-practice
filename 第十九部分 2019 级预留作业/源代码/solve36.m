clear;clc;
[myinfo,color]=config();

t=0:0.1:2*pi;
y1=sin(t);
y2=cos(2*t);
y3=sin(t)+cos(t);
y4=3*t;
y5=2*log(t);
y6=exp(t);
subplot(2,3,1);
plot(t,y1);
title('sin(t)');
subplot(2,3,2);
plot(t,y2);
title('cos(t)');
subplot(2,3,3);
plot(t,y3);
title('sin(t)+cos(t)');
subplot(2,3,4);
plot(t,y4);
title('3t');
subplot(2,3,5);
plot(t,y5);
title('2ln(t)');
subplot(2,3,6);
plot(t,y6);
title('e^t');
suptitle(myinfo);
